public class Marker {

    private String latitud_marker;
    private String longitud_marker;
    private String nombre_marker;
    private String descripcion_marker;
    private String categoria_marker;
    private String subcategoria_marker;
    private String direccion_marker;
    private String imagen_marker;

    public String getLatitud_marker() {
        return latitud_marker;
    }

    public void setLatitud_marker(String latitud_marker) {
        this.latitud_marker = latitud_marker;
    }

    public String getLongitud_marker() {
        return longitud_marker;
    }

    public void setLongitud_marker(String longitud_marker) {
        this.longitud_marker = longitud_marker;
    }

    public String getNombre_marker() {
        return nombre_marker;
    }

    public void setNombre_marker(String nombre_marker) {
        this.nombre_marker = nombre_marker;
    }

    public String getDescripcion_marker() {
        return descripcion_marker;
    }

    public void setDescripcion_marker(String descripcion_marker) {
        this.descripcion_marker = descripcion_marker;
    }

    public String getCategoria_marker() {
        return categoria_marker;
    }

    public void setCategoria_marker(String categoria_marker) {
        this.categoria_marker = categoria_marker;
    }

    public String getSubcategoria_marker() {
        return subcategoria_marker;
    }

    public void setSubcategoria_marker(String subcategoria_marker) {
        this.subcategoria_marker = subcategoria_marker;
    }

    public String getDireccion_marker() {
        return direccion_marker;
    }

    public void setDireccion_marker(String direccion_marker) {
        this.direccion_marker = direccion_marker;
    }

    public String getImagen_marker() {
        return imagen_marker;
    }

    public void setImagen_marker(String imagen_marker) {
        this.imagen_marker = imagen_marker;
    }
}
